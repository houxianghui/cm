package com.yly.drools;

public class Func {
	private String operAct;
	private String manufacId;
	private String prodId;
	private String applyAttr;
	private String func;
	public String getOperAct() {
		return operAct;
	}
	public void setOperAct(String operAct) {
		this.operAct = operAct;
	}
	public String getManufacId() {
		return manufacId;
	}
	public void setManufacId(String manufacId) {
		this.manufacId = manufacId;
	}
	public String getProdId() {
		return prodId;
	}
	public void setProdId(String prodId) {
		this.prodId = prodId;
	}
	public String getApplyAttr() {
		return applyAttr;
	}
	public void setApplyAttr(String applyAttr) {
		this.applyAttr = applyAttr;
	}
	public String getFunc() {
		return func;
	}
	public void setFunc(String func) {
		this.func = func;
	}
	public String getPara() {
		return para;
	}
	public void setPara(String para) {
		this.para = para;
	}
	private String para;

}
